-- ============================================
-- Job Pipeline Schema for Supabase
-- ============================================

-- Custom ENUM types
CREATE TYPE job_field AS ENUM ('AI_ENGINEER', 'TEACHING', 'PHD', 'MARKETING');
CREATE TYPE application_status AS ENUM ('NEW', 'CV_TWEAKED', 'APPLIED', 'REJECTED');

-- Table: sponsored_companies
-- Stores companies known to offer visa sponsorship
CREATE TABLE IF NOT EXISTS sponsored_companies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name TEXT NOT NULL UNIQUE,
    industry TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Add index for fast company name lookups
CREATE INDEX IF NOT EXISTS idx_sponsored_companies_name
    ON sponsored_companies(company_name);

-- Table: jobs
-- Stores discovered job openings with sponsorship
CREATE TABLE IF NOT EXISTS jobs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name TEXT NOT NULL,
    job_title TEXT NOT NULL,
    job_url TEXT NOT NULL UNIQUE, -- Prevents duplicates
    description TEXT NOT NULL,
    field job_field NOT NULL,
    status application_status NOT NULL DEFAULT 'NEW',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Add indexes for performance
CREATE INDEX IF NOT EXISTS idx_jobs_status ON jobs(status);
CREATE INDEX IF NOT EXISTS idx_jobs_field ON jobs(field);
CREATE INDEX IF NOT EXISTS idx_jobs_company ON jobs(company_name);
CREATE INDEX IF NOT EXISTS idx_jobs_created ON jobs(created_at DESC);

-- Table: master_cvs
-- Stores one master CV template per job field/niche
CREATE TABLE IF NOT EXISTS master_cvs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    field job_field NOT NULL UNIQUE, -- One master CV per niche
    cv_content_markdown TEXT NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Table: tweaked_cvs
-- Stores AI-tailored CVs for specific jobs
CREATE TABLE IF NOT EXISTS tweaked_cvs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id BIGINT NOT NULL REFERENCES jobs(id) ON DELETE CASCADE UNIQUE,
    tailored_cv_markdown TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Add index for job lookups
CREATE INDEX IF NOT EXISTS idx_tweaked_cvs_job_id ON tweaked_cvs(job_id);

-- ============================================
-- Helper Functions
-- ============================================

-- Function to automatically update updated_at timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Trigger for jobs table
DROP TRIGGER IF EXISTS update_jobs_updated_at ON jobs;
CREATE TRIGGER update_jobs_updated_at
    BEFORE UPDATE ON jobs
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- Trigger for master_cvs table
DROP TRIGGER IF EXISTS update_master_cvs_updated_at ON master_cvs;
CREATE TRIGGER update_master_cvs_updated_at
    BEFORE UPDATE ON master_cvs
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

