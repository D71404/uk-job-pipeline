-- ============================================
-- Views for Dashboard
-- ============================================

-- View: Recent jobs needing CV tailoring
CREATE OR REPLACE VIEW jobs_pending_cv AS
SELECT
    j.id,
    j.company_name,
    j.job_title,
    j.job_url,
    j.field,
    j.status,
    j.created_at
FROM jobs j
WHERE j.status = 'NEW'
ORDER BY j.created_at DESC;

-- View: Jobs by field with stats
CREATE OR REPLACE VIEW jobs_stats_by_field AS
SELECT
    field,
    COUNT(*) as total_jobs,
    COUNT(*) FILTER (WHERE status = 'NEW') as new_jobs,
    COUNT(*) FILTER (WHERE status = 'CV_TWEAKED') as cv_tweaked,
    COUNT(*) FILTER (WHERE status = 'APPLIED') as applied,
    COUNT(*) FILTER (WHERE status = 'REJECTED') as rejected
FROM jobs
GROUP BY field;

